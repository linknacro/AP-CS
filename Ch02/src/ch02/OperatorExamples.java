
package ch02;
public class OperatorExamples {

    public static void main(String[] args) {
        int numA = 10 / 4;
        System.out.println("numA: " + numA);

        double numB = 10 / 4;
        System.out.println("numB: " + numB);

        double numC = 10.0 / 4;
        System.out.println("numC: " + numC);

        double numD = 5 / 2 + 3;
        System.out.println("numD: " + numD);

        double numE = 5 / 2 + 3.0;
        System.out.println("numE: " + numE);

        double numF = 5 / (2 + 3.0);
        System.out.println("numF: " + numF);

        double numG = 5 + 2 * 3;
        System.out.println("numG: " + numG);

        double numH = 10 * 2 / 4;
        System.out.println("numH: " + numH);

        double numJ = 10 / 2 * 4;
        System.out.println("numJ: " + numJ);

        
        
        int numK = 10 / 3;
        System.out.println("numK: " + numK);

        int numL = 15 / 4;
        System.out.println("numL: " + numL);

        int numM = 100 / 26;
        System.out.println("numM: " + numM);
        
        int numN = 10 % 3;
        System.out.println("numN: " + numN);

        int numP = 15 % 4;
        System.out.println("numP: " + numP);

        int numR = 100 % 26;
        System.out.println("numR: " + numR);
        
        double numS = 5;
        System.out.println(numS);
        
        double numT = 5;
        System.out.println(numT);

    }

}
